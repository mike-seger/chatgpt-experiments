CREATE TABLE MY_TABLE (
  ID NUMBER(10),
  DATA BLOB
);

INSERT INTO MY_TABLE (ID, DATA) VALUES (1, EMPTY_BLOB());

DECLARE
  l_blob BLOB;
BEGIN
  SELECT DATA INTO l_blob FROM MY_TABLE WHERE ID = 1 FOR UPDATE;
  DBMS_LOB.CREATETEMPORARY(l_blob, TRUE);
  DBMS_LOB.OPEN(l_blob, DBMS_LOB.LOB_READWRITE);
  DBMS_LOB.WRITE(l_blob, LENGTH('test data'), 1, 'test data');
  DBMS_LOB.CLOSE(l_blob);
  UPDATE MY_TABLE SET DATA = l_blob WHERE ID = 1;
  COMMIT;
END;
